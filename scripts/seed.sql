-- 清空并重建预订表（首次部署用，会清除所有已有数据）
DROP TABLE IF EXISTS dive_bookings CASCADE;

CREATE TABLE dive_bookings (
  id SERIAL PRIMARY KEY,
  name VARCHAR(128) NOT NULL,
  phone VARCHAR(32) NOT NULL,
  experience VARCHAR(64) NOT NULL,
  booking_date VARCHAR(32),
  message TEXT DEFAULT '',
  status VARCHAR(32) DEFAULT 'pending' NOT NULL,
  remark TEXT DEFAULT '',
  created_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
);

CREATE INDEX dive_bookings_status_idx ON dive_bookings(status);
CREATE INDEX dive_bookings_created_at_idx ON dive_bookings(created_at);

